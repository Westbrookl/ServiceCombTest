package com.type;

public class NgKsi {
	ScType tsc;
	short ksi;
	public NgKsi(ScType tsc, short ksi) {
		super();
		this.tsc = tsc;
		this.ksi = ksi;
	}
	public ScType getTsc() {
		return tsc;
	}
	public void setTsc(ScType tsc) {
		this.tsc = tsc;
	}
	public short getKsi() {
		return ksi;
	}
	public void setKsi(short ksi) {
		this.ksi = ksi;
	}
	
}
