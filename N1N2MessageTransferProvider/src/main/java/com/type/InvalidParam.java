package com.type;

public class InvalidParam {
	String param;
	String reason;
	public InvalidParam(String param) {
		super();
		this.param = param;
	}
	public String getParam() {
		return param;
	}
	public void setParam(String param) {
		this.param = param;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	
}
